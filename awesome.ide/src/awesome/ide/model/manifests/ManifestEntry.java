package awesome.ide.model.manifests;

public class ManifestEntry {
	private String key;
	private String value;

	public String getKey() {
		return key;
	}
	protected void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	protected void setValue(String value) {
		this.value = value;
	}
}
