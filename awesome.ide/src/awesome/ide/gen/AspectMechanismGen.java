package awesome.ide.gen;

public class AspectMechanismGen
{
  protected static String nl;
  public static synchronized AspectMechanismGen create(String lineSeparator)
  {
    nl = lineSeparator;
    AspectMechanismGen result = new AspectMechanismGen();
    nl = null;
    return result;
  }

  public final String NL = nl == null ? (System.getProperties().getProperty("line.separator")) : nl;
  protected final String TEXT_1 = "package ";
  protected final String TEXT_2 = ";" + NL + "" + NL + "import awesome.platform.*;" + NL + "import java.util.ArrayList;" + NL + "import java.util.Iterator;" + NL + "import java.util.List;" + NL + "import org.aspectj.weaver.IClassFileProvider;" + NL + "import org.aspectj.weaver.bcel.BcelShadow;" + NL + "import org.aspectj.weaver.bcel.UnwovenClassFile;" + NL + "" + NL + "public aspect ";
  protected final String TEXT_3 = " extends AwesomeMechanism {" + NL + "\t/**" + NL + "\t* Holds all the aspects of the DSAL that were generated by the front-end" + NL + "\t*/" + NL + "\tprivate List<AspectClass> aspectClasses = new ArrayList<AspectClass>();" + NL + "\t" + NL + "\tpublic String getId() {" + NL + "\t\treturn \"";
  protected final String TEXT_4 = "\";" + NL + "\t}" + NL + "\t" + NL + "\t@Override" + NL + "\tpublic List<IEffect> match(BcelShadow shadow) {" + NL + "\t\treturn super.match(shadow);\t" + NL + "\t}" + NL + "\t" + NL + "\t// initializes aspectClasses..." + NL + "\t@Override" + NL + "\tpublic void setInputFiles(IClassFileProvider inputClasses) {" + NL + "\t\tsuper.setInputFiles(inputClasses);" + NL + "\t\t" + NL + "\t\tSystem.out.println(getId() + \".setInputFiles...\");" + NL + "\t\tfor (Iterator i = inputClasses.getClassFileIterator(); i.hasNext();) {" + NL + "\t\t\tUnwovenClassFile classFile = (UnwovenClassFile) i.next();" + NL + "\t\t\t \t" + NL + "\t\t\tif(!AwesomeCore.hasAspectAnnotation(classFile) || " + NL + "\t\t\t\t!AwesomeCore.belongsToAspectMechanism(classFile.getJavaClass(), getId())) {" + NL + "\t\t\t\tcontinue;" + NL + "\t\t\t}" + NL + "\t\t\tSystem.out.println(\"Found \" + getId() + \" aspect class: \" + classFile.getClassName() + \".\");" + NL + "\t\t\tAspectClass ac = AwesomeCore.create(classFile);" + NL + "\t\t\taspectClasses.add(ac);" + NL + "\t\t}\t\t " + NL + "\t}" + NL + "}";

  public String generate(Object argument)
  {
    final StringBuffer stringBuffer = new StringBuffer();
    String packageName = ((String[])argument)[0];
    String aspectName = ((String[])argument)[1];
    String id = ((String[])argument)[2];
    stringBuffer.append(TEXT_1);
    stringBuffer.append(packageName);
    stringBuffer.append(TEXT_2);
    stringBuffer.append(aspectName);
    stringBuffer.append(TEXT_3);
    stringBuffer.append(id);
    stringBuffer.append(TEXT_4);
    return stringBuffer.toString();
  }
}
